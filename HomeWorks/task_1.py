# 8个表达式使用整型数进行运算得8，使用浮点数运算等于8.88
a = 2
b = 2.22

# 加法运算
i = a + a + a + a
f = b + b + b + b
print("%d" % i)
print("%.2f" % f)

# 减法运算
i = 10 - a
f = 11.1 - b
print("%d" % i)
print("%.2f" % f)

# 乘法运算
i = 4 * a
f = 4 * b
print("%d" % i)
print("%.2f" % f)

# 除法运算
i = 16 / a
f = 19.7136 / b
print("%d" % i)
print("%.2f" % f)
